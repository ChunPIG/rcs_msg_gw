package com.suntek.rcs.msg.gw.common.exception;

public class CommainException extends Exception {

	private static final long serialVersionUID = -7208211039439553557L;

	public CommainException() {
		super();
	}

	public CommainException(String message, Throwable cause) {
		super(message, cause);
	}

	public CommainException(String message) {
		super(message);
	}

	public CommainException(Throwable cause) {
		super(cause);
	}

}
